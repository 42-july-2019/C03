char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int length;
	unsigned int i;

	length = 0;
	i = 0;
	while (dest[length] != '\0')
		length++;
	while (i < nb && src[i] != '\0')
	{
		dest[length + i] = src[i];
		i++;
	}
	dest[length + i] = '\0';
	return (dest);
}
